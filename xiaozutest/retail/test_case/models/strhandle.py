def StrHandle(str):
    try:
        lowerCase = 0
        upperCase = 0
        number = 0
        other = 0
        for stritem in str:
            if stritem.islower():
                lowerCase += 1
            elif stritem.isupper():
                upperCase += 1
            elif stritem.isdigit():
                number += 1
            else:
                other += 1
        return lowerCase,upperCase,number,other
    except Exception as e:
        log.logger.exception('string handle error,please check!')
        raise e
